function displayTrainers(category) {
    let html = `
        <nav class="nav nav-pills nav-justified">
            <a class="nav-item nav-link ${category === 'cardio' ? 'active' : ''}" data-toggle="tab" href="#cardio" onclick="displayTrainers('cardio')">Кардио</a>
            <a class="nav-item nav-link ${category === 'strength' ? 'active' : ''}" data-toggle="tab" href="#strength" onclick="displayTrainers('strength')">Силовые</a>
            <a class="nav-item nav-link ${category === 'functional' ? 'active' : ''}" data-toggle="tab" href="#functional" onclick="displayTrainers('functional')">Функциональные</a>
        </nav>
        <div class="tab-content mt-4">
            <div class="tab-pane fade show active">
                <div class="row">
    `;
    data[category].forEach(item => {
        html += `
            <div class="col-md-4 mb-4">
                <div class="card">
                    <img src="${item.img}" class="card-img-top" alt="${item.name}">
                    <div class="card-body">
                        <h5 class="card-title">${item.name}</h5>
                        <p class="card-text">${item.description}</p>
                        <button class="btn btn-primary" onclick="showDetails('${category}', '${item.name}')">Подробнее</button>
                    </div>
                </div>
            </div>
        `;
    });
    html += `
                    </div>
                </div>
            </div>
        `;
    $('#trainers-section').html(html);
}

